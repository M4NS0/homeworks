package modelos;

public class Motorista {
    private int id;
    private int motoristacodigo;
    private String nome;
    private String endereco;
    private String telefone;
    private String email;
    private String tipocnh;
    private String turno;
    private String valorhoraextra;

    public Motorista(){

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMotoristacodigo() {
        return motoristacodigo;
    }

    public void setMotoristacodigo(int motoristacodigo) {
        this.motoristacodigo = motoristacodigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTipocnh() {
        return tipocnh;
    }

    public void setTipocnh(String tipocnh) {
        this.tipocnh = tipocnh;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    public String getValorhoraextra() {
        return valorhoraextra;
    }

    public void setValorhoraextra(String valorhoraextra) {
        this.valorhoraextra = valorhoraextra;
    }

    // Getters and setters
}
