    // Metodo que realiza conexao com o banco, faz uma instrucao Query(select)
    // para jogar na JTable atraves do modelo de tabela (ModelTabel.java)
    public void carregarTable(Paciente objPaciente) {

        objDAO = new AcessoDados();
        ArrayList dados = new ArrayList();
        if (buscar) {
            dados = objDAO.buscar(objPaciente);
        } else {
            objPaciente = new Paciente();
            dados = objDAO.listarTodos();
        }
        String[] colunas = objPaciente.getPacienteColunas();

        TableModel modelo = new TableModel(dados, colunas);

        tblPacientes.setModel(modelo);
        tblPacientes.getColumnModel().getColumn(0).setPreferredWidth(80);
        tblPacientes.getColumnModel().getColumn(0).setResizable(false);
        tblPacientes.getColumnModel().getColumn(1).setPreferredWidth(120);
        tblPacientes.getColumnModel().getColumn(1).setResizable(false);
        tblPacientes.getColumnModel().getColumn(2).setPreferredWidth(250);
        tblPacientes.getColumnModel().getColumn(2).setResizable(false);
        tblPacientes.getTableHeader().setReorderingAllowed(false);
    }
    
    // Metodo responsavel por selecionar um registro ao clicar ou seguir com as setas do teclado
    // na JTable, e realizar a vinculacao do mesmo nos TextFields
