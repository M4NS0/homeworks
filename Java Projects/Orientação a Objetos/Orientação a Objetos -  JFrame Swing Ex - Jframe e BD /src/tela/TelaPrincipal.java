    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        objPaciente.setPacienteId(txtPacienteID.getText());

        // fazendo a validação dos dados
        if ((txtPacienteID.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Informe valores para os campos");
        } else {
            // instanciando a classe AcessoDados do pacote dao e criando seu objeto dao
            objDAO = new AcessoDados();
            objDAO.deletar(objPaciente);
            JOptionPane.showMessageDialog(null, "Paciente Removido com Sucesso! ");
        }

        carregarTable(null);

        // apaga os dados preenchidos nos campos de texto
        setClear();
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        objPaciente = new Paciente();
        objPaciente.setPacienteNome(txtPacienteNome.getText());
        objPaciente.setPacienteCpf(txtPacienteCPF.getText());

        // fazendo a validação dos dados
        if ((!txtPacienteNome.getText().isEmpty()) || (!txtPacienteCPF.getText().isEmpty())) {
            // instanciando a classe AcessoDados do pacote dao e criando seu objeto dao
            objDAO = new AcessoDados();
            buscar = true;
            carregarTable(objPaciente);
        }else{
            buscar = false;
            carregarTable(null);
        }

        // apaga os dados preenchidos nos campos de texto
        setClear();
    }//GEN-LAST:event_btnBuscarActionPerformed

