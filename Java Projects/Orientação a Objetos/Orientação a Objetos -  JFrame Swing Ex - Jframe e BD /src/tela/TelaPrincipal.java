    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        objPaciente = new Paciente();
        objPaciente.setPacienteId(txtPacienteID.getText());
        objPaciente.setPacienteNome(txtPacienteNome.getText());
        objPaciente.setPacienteCpf(txtPacienteCPF.getText());
        objPaciente.setPacienteMae(txtPacienteMae.getText());
        objPaciente.setPacienteTelefone(txtPacienteTelefone.getText());
        objPaciente.setPacienteInternacao(txtPacienteInternacao.getText());
        objPaciente.setPacienteAlta(txtPacienteAlta.getText());

        // fazendo a validação dos dados
        if ((txtPacienteNome.getText().isEmpty()) ||
                (txtPacienteCPF.getText().isEmpty()) ||
                (txtPacienteMae.getText().isEmpty()) ||
                (txtPacienteTelefone.getText().isEmpty()) ||
                (txtPacienteInternacao.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Informe valores para os campos");
        } else {
            // instanciando a classe AcessoDados do pacote dao e criando seu objeto dao
            objDAO = new AcessoDados();
            objDAO.salvar(objPaciente);
            JOptionPane.showMessageDialog(null, "Paciente " + txtPacienteNome.getText() + " inserido com sucesso! ");
        }

        carregarTable(null);

        // apaga os dados preenchidos nos campos de texto
        setClear();
    }//GEN-LAST:event_btnSalvarActionPerformed

