#include <iostream>
using namespace std;

int main() {

	int i = 0;

	while (i <= 5) {
		cout << "Hello #" << i << endl;

		i++; // i = i + 1;

	}
	cout << "\nProgram quitting..." << endl;

	return 0;
}
