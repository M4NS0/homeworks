# Subtracao
su = 4 - 3
print(su)

# Multiplicacao
mu = 3 * 3
print(mu)

# Divisão
di = 3 / 2
print(di)

# Potencia
po = 4 ** 2
print(po)

# Modulo
mo = 10 % 3
print(mo)
